/**
 * @param {number} n
 * @return {number}
 */
var nthUglyNumber = function (n) {
    var list = [],
        l,min;
    while (list.length < n) {
        l = list.length;
        min = 0;
        for (var i = 0; i < l; i++) {
            min = list[i]
        }


    }

};

console.log(nthUglyNumber(10));